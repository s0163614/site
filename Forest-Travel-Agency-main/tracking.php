            <h1>Трекинг походы</h1>
            <p>
              <b>Треккинг</b> — это многодневный поход по гористой местности.
              Путешествие проходит по заранее проложенному и промаркированному
              маршруту. Для ночевок используются кемпинговые стоянки или, реже,
              свои палатки. Все необходимое, начиная от еды, заканчивая сменной
              одеждой, упаковывается в <b>треккинговый рюкзак</b>.
            </p>
            <div class="img-tracking">
              <img
                src="./img/articles/tracking-hiking.jpg"
                alt="Трекинг походы"
              />
            </div>
            <p>
              При занятии треккингом скорость передвижения небольшая, ведь за
              спиной тяжелый рюкзак, а под ногами горные тропы. <b>Цель</b> — не
              пройти от старта до финиша как можно быстрее, а увидеть новые
              пейзажи, познакомиться с природными или историческими памятниками,
              получить удовольствие.
            </p>
            <p>
              Ежедневный переход обычно занимает 5–7 часов. Это зависит от
              сложности маршрута и выносливости участников. Треккинг не
              предполагает выход за отметку высоты в 2000м. Горы дают уникальную
              возможность увидеть, как быстро меняется животный мир с изменением
              высоты.
            </p>
            <h2>Кому подойдет треккинг?</h2>
            <p>
              Занятия базовым треккингом не требуют специализированной
              подготовки. Вы не будете карабкаться по отвесным скалам и
              форсировать горные реки. Но многодневный поход с рюкзаком
              потребует определенной <b>силы и выносливости</b>. Резкая перемена
              погоды тоже может добавить неприятных ощущений.
            </p>
            <h3>Особенности треккинга</h3>
            <p>
              Маршрут рассчитывается так, чтобы переходы не были чересчур
              утомительны для любого участника, независимо от возраста и
              физической подготовки.
            </p>
            <h4>Почему стоит заняться треккингом?</h4>
            <p>
              Установить здоровый режим для организма, благодаря дозировке
              физической активности и отдыха. <b>Не надо ничего решать</b> -
              треккинг проходит по установленным правилам, а важные решения
              принимают местные гиды. <b>Полная перестройка</b> - человек
              погружается в непривычную среду, где он должен перенастроить свой
              организм, преодолеть сопротивление новой среды и в итоге получить
              удовольствие.
            </p>
